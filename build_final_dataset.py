def pad_to_200(sent_list, label):
    s = sent_list.copy()
    i = 0
    while len(s) < 200:
        base = sent_list[i % len(sent_list)]
        # small synthetic variation to avoid exact duplicates:
        variant = base
        if len(base) > 10:
            variant = base.rstrip(" ।,.") + "."
        s.append(variant + f" [{label}-{len(s)+1}]")
        i += 1
    return s[:200]

