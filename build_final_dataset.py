# helper to create record from text, label, compound (if None, generate realistic)
def make_record(text, label, compound_value=None):
    global id_counter
    if compound_value is None:
        # generate compound based on label
        if label == "normal":
            compound_value = round(random.uniform(0.0, 0.8), 2)
        elif label == "depression":
            compound_value = round(random.uniform(-0.95, -0.5), 2)
        elif label == "stress":
            compound_value = round(random.uniform(-0.8, -0.15), 2)
        elif label == "anxiety":
            compound_value = round(random.uniform(-0.9, -0.2), 2)
        else:
            compound_value = 0.0
    compound_value = clamp(compound_value, -1.0, 1.0)
    intensity = compound_to_intensity(compound_value)
    rec = {
        "id": id_counter,
        "text": text.strip(),
        "emotion": label,
        "compound": compound_value,
        "intensity": intensity
    }
    id_counter += 1
    return rec

