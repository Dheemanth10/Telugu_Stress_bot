def compound_to_intensity(compound):
    """
    Convert compound (-1 .. +1) to intensity (1 .. 10).
    Inverts polarity so more negative -> higher intensity.
    """
    intensity = ((-compound + 1) / 2) * 9 + 1
    return int(round(max(1, min(10, intensity))))

def clamp(v, a=-1.0, b=1.0):
    return max(a, min(b, v))

